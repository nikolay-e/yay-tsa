#!/usr/bin/env node
import { readFileSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { parse as parseYaml } from 'yaml';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');
const yamlPath = resolve(repoRoot, 'shared-constants.yaml');
const outPath = resolve(repoRoot, 'packages/core/src/generated/constants.ts');

const doc = parseYaml(readFileSync(yamlPath, 'utf8'));
const constants = doc?.constants ?? {};
const enums = doc?.enums ?? {};

const lines = [
  '// AUTOGENERATED from /shared-constants.yaml — do not edit.',
  '// Run `npm run gen:constants` to regenerate.',
  '',
];

for (const [name, value] of Object.entries(constants)) {
  if (typeof value !== 'number') {
    throw new TypeError(`Constant ${name} must be a number, got ${typeof value}`);
  }
  lines.push(`export const ${name} = ${value};`);
}
lines.push('');

for (const [name, values] of Object.entries(enums)) {
  if (!Array.isArray(values) || values.some(v => typeof v !== 'string')) {
    throw new TypeError(`Enum ${name} must be an array of strings`);
  }
  const union = values.map(v => `'${v}'`).join(' | ');
  lines.push(
    `export type ${name} = ${union};`,
    `export const ${name}Values: readonly ${name}[] = [`,
    ...values.map(v => `  '${v}',`),
    '] as const;',
    ''
  );
}

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, lines.join('\n'));
process.stdout.write(`generated: ${outPath}\n`);
